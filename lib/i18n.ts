const strings: Record<Locale, typeof en> = { en, es };

export function t(key: keyof typeof en, vars?: Record<string, string>): string {
  let str: string = strings[locale][key] ?? strings.en[key];
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      str = str.replace(new RegExp(`\\{${k}\\}`, "g"), v);
    }
  }
  return str;
}

const OCCASION_KEYS: Record<string, keyof typeof en> = {
  office: "occOffice",
  wfh: "occWfh",
  gym: "occGym",
  "date night": "occDateNight",
  dinner: "occDinner",
  errand: "occErrand",
  travel: "occTravel",
  gallery: "occGallery",
  event: "occEvent",
  casual: "occCasual",
  other: "occOther",
};

// Occasions are stored as fixed English keys in the DB; this only
// localizes how they're displayed.
export function occasionLabel(occ: string | null | undefined): string {
  if (!occ) return "";
  const key = OCCASION_KEYS[occ];
  return key ? t(key) : occ.toUpperCase();
}
